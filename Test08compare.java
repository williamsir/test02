class Test08compare{
	public static void main(String[] args){
		int i1 = 10;
		int i2 = 20;
		System.out.println(i2>i1); 	//输出true
		System.out.println(i2 >= i1);	//输出true
		System.out.println(i2 <= i1);	//输出false
		System.out.println(i2 == i1);	//输出false
		System.out.println(i2 != i1);	//输出true

	}
	}